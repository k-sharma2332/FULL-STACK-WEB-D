//Arithmetic operator
let a = 10;
let b = 5;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a**b);
console.log(a/b);
//Unary Operator(++,--)
console.log(a++); //10
console.log(++a); //12
//Assignment Operator(=,+=,-=,*=,/=,%=)
b=a;
console.log(b);
//Comparision Operator
let age = 18;
console.log(age>18); //false
console.log(age<=18);//true
console.log(a==b);
