// Logical Operator
let marks = 90;

//Logical And

if(marks >= 33 && marks >= 80){
    console.log("pass");
    console.log("A+");
}

//Logical Or

if(marks >= 33  || marks >= 80){
    console.log("pass");
    console.log("A+");
}

//Logical Not Operator

if((marks > 33 && marks <= 80) || !false){
    console.log("pass");
   
}